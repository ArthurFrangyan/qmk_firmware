bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if(keycode == TAB_SET)
  {
    if(record->event.pressed)
    {
      if (!record->tap.count) {
        isSetOn = true;
      }
    }
    else
    {
      if (!record->tap.count) {
        isSetOn = false;
      }
    }
    return true;
  }
  else if(keycode == SET)
  {
    if(record->event.pressed)
    {
      isSetOn = true;
    }
    else
    {
      isSetOn = false;
    }
    return true;
  }
  if(isSetOn || SETKeyPressed)
  {
    void mod_modKey(uint8_t, uint8_t, bool, bool);
    void modKey(uint8_t , uint8_t , bool);
    uint8_t modIndex;
    uint8_t keyIndex = keycode;

    bool isKeyPressed = true;
    bool isKeyComplicated = true;

    uint8_t col = record->event.key.col;
    uint8_t row = record->event.key.row;
    void correctMatrix(uint8_t* row, uint8_t* col);
    correctMatrix(&row, &col);

    switch (keycode)
    {
      case F_:
        keyIndex = KC_F;
        break;
      case D_:
        keyIndex = KC_D;
        break;
      case S_:
        keyIndex = KC_S;
        break;
      case A_:
        keyIndex = KC_A;
        break;
      case C_:
        keyIndex = KC_C;
        break;
      case X_:
        keyIndex = KC_X;
        break;
      case V_:
        keyIndex = KC_V;
        break;

      case J_:
        keyIndex = KC_J;
        break;
      case K_:
        keyIndex = KC_K;
        break;
      case L_:
        keyIndex = KC_L;
        break;
      case SCLN_:
        keyIndex = KC_SCLN;
        break;
      case COMM_:
        keyIndex = KC_COMM;
        break;
      case DOT_:
        keyIndex = KC_DOT;
        break;
      case ESC_M_:
        keyIndex = KC_ESC;
        break;
      default:
        isKeyComplicated = false;
        break;
    }
    if(row < 4)
    {
      switch (col)
      {
        case 0:
        case 1:
        case 2:
          modIndex = KC_LWIN;
          break;
        case 3:
          modIndex = KC_LALT;
          break;
        case 4:
          modIndex = KC_LCTL;
          break;
        case 5:
        case 6:
          modIndex = KC_LSFT;
          break;
        default:
          isKeyPressed = false;
          break;
      }
    }
    else if(row > 4 && row < 9)
    {
      switch (col)
      {
        case 0:
        case 1:
        case 2:
          modIndex = KC_RWIN;
          break;
        case 3:
          modIndex = KC_RALT;
          break;
        case 4:
          modIndex = KC_RCTL;
          break;
        case 5:
        case 6:
          modIndex = KC_RSFT;
          break;
        default:
          isKeyPressed = false;
          break;
      }
    }
    else
    {
      isKeyPressed = false;
    }
    if(isKeyPressed)
    {
      if((row == 2 || row == 7) && col > 1 && col < 6 && isKeyComplicated)
      {
        // modKey(modIndex, keyIndex, record->event.pressed);
        mod_modKey(modIndex, keyIndex, record->event.pressed, record->tap.count);
      }
      else
      {
        modKey(modIndex, keyIndex, record->event.pressed);
      }
      return false;
    }
  }
  switch (keycode) {
    case MKC_00:
      if (record->event.pressed) {
        tap_code(KC_0);
        tap_code(KC_0);
      }
      return false;
    case QWERTY:
      if (record->event.pressed) {
        _PAD_index = _NUMPAD;
        set_single_persistent_default_layer(_QWERTY);
        layer_clear();
      }
      return false;
    case GEMINI:
      if (record->event.pressed) {
        // set_single_persistent_default_layer(_GEMINI);

        layer_move(_GEMINI);
      }
      return false;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
        // layer_off(_SWP_HND);
      }
      return false;
    case SPC_LOW:
    case C_:
    case COMM_:
      if(record->event.pressed)
      {
        if (!record->tap.count) {
          layer_on(_LOWER);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
          layer_off(_LOWER);
        }
      }
      else
      {
        if (!record->tap.count) {
          layer_off(_LOWER);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
          layer_off(_LOWER);
        }
      }
      return true;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
    case SPC_REZ:
    case BSPC_REZ:
    case X_:
    case DOT_:
      if(record->event.pressed)
      {
        if (!record->tap.count) {
          layer_on(_RAISE);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
          layer_off(_RAISE);
        }
      }
      else
      {
        if (!record->tap.count) {
          layer_on(_RAISE);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
          layer_off(_RAISE);
        }
      }
      return true;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
    case MENU:
      if (record->event.pressed) {
        layer_on(_MENU);
      }
      else{
        layer_off(_MENU);
      }
      return false;
   case MENU_R:
      if (record->event.pressed) {
        layer_on(_MENU);
        menu_R = true;
      }
      else{
        layer_off(_MENU);
        menu_R = false;
      }
      return false;
    case GAME:
      if (record->event.pressed) {
        _PAD_index = _GAME;
        layer_move(_GAME);
      }
      return false;
    case GAME_2:
      if (record->event.pressed) {
        _PAD_index = _GAME_2;
        layer_move(_GAME_2);
      }
      return false;
    case GAME_N:
      if (record->event.pressed) {
        layer_on(_GAME_N);
      } else {
        layer_off(_GAME_N);
      }
      return false;
    case GAME_E:
      if (record->event.pressed) {
        layer_move(_GAME_E);
      }
      return false;
    case NUMPAD:
      if (record->event.pressed) {
        layer_move(_NUMPAD);
      }
      return false;
    case BRUSH:
      if (record->event.pressed) {
        layer_move(_BRUSH);
      }
      return false;
    case LOWER_F:
      if (record->event.pressed) {
        layer_move(_LOWER);
      }
      return false;
    case RAISE_F:
      if (record->event.pressed) {
        layer_move(_RAISE);
      }
      return false;
    case PAD:
      if (record->event.pressed) {
        if (IS_LAYER_ON(_PAD_index))
          layer_off(_PAD_index);
        else
          layer_on(_PAD_index);
      }
      return false;
  }
  
  // #ifdef CONSOLE_ENABLE
  //   print("KL:jjjj");
  // #endif 
    return true;
}

