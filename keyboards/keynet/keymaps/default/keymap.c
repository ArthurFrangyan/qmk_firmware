void mod_modKey(uint8_t mod, uint8_t key, bool isPressed, bool isTapped)
{
  if(isPressed)
  {
    SETKeyPressed++;
    if (isTapped) {
      register_code(mod);
      register_code(key);
    }
    else
    {
      register_code(mod);
    }
  }
  else
  {
    SETKeyPressed--;
    if (isTapped) {
      unregister_code(mod);
      unregister_code(key);
    }
    else
    {
      unregister_code(mod);
    }
  }
}

void modKey(uint8_t mod, uint8_t key, bool isPressed)
{
  if(isPressed)
  {
    SETKeyPressed++;
    register_code(mod);
    register_code(key);
  }
  else
  {
    SETKeyPressed--;
    unregister_code(mod);
    unregister_code(key);
  }
}

void correctMatrix(uint8_t* row, uint8_t* col)
{
  if(*row == 4 && *col == 7)
  {
    *row = 3;
    *col = 8;
  }
  else if(*row == 9 && *col == 6)
  {
    *col = 8;
  }
  else if(*row == 4)
  {
    if(*col == 0 || *col == 1)
      *col += 1;
    *col += 2;
  }
  else if(*row == 9)
  {
    *col+=3;
  }

}