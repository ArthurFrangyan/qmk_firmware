bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
        layer_clear();
      }
      return false;
      break;
    case GEMINI:
      if (record->event.pressed) {
        // set_single_persistent_default_layer(_GEMINI);
        layer_move(_GEMINI);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        layer_off(_SWP_HND);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {

        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case MENU_L:
      if (record->event.pressed) {
        layer_on(_MENU_L);
      }
      else{
        layer_off(_MENU_L);
      }
      return false;
      break;
    case MENU_R:
      if (record->event.pressed) {
        layer_on(_MENU_R);
      }
      else{
        layer_off(_MENU_R);
      }
      return false;
      break;
    case U_LOWER:
      if (record->event.pressed) {
        layer_on(_U_LOWER);
      }
      else{
        layer_off(_U_LOWER);
      }
      return false;
      break;
    case SET:
      if (record->event.pressed) {
        layer_on(_SET);
      }
      else{
        layer_off(_SET);
      }
      return false;
      break;
    case GAME:
      if (record->event.pressed) {
        layer_move(_GAME);
      }
      return false;
      break;
    case GAME_TWO:
      if (record->event.pressed) {
        layer_move(_GAME_TWO);
      }
      return false;
      break;
    case SWP_HND:
      if (record->event.pressed) {
        layer_on(_SWP_HND);
      } else {
        layer_off(_SWP_HND);
      }
      return false;
      break;
    case GAME_N:
      if (record->event.pressed) {
        layer_on(_GAME_N);
      } else {
        layer_off(_GAME_N);
      }
      return false;
      break;
    case GAME_E:
      if (record->event.pressed) {
        if (IS_LAYER_ON(_GAME_E))
          layer_off(_GAME_E);
        else
          layer_on(_GAME_E);
      }
      return false;
      break;
    case HDSWP_F:
      if (record->event.pressed) {
        if (IS_LAYER_ON(_SWP_HND))
          layer_off(_SWP_HND);
        else
          layer_on(_SWP_HND);
      }
      return false;
      break;
    case NUMPAD:
      if (record->event.pressed) {
        layer_move(_NUMPAD);
      }
      return false;
      break;
    case BRUSH:
      if (record->event.pressed) {
        layer_move(_BRUSH);
      }
      return false;
      break;
    case LOWER_F:
      if (record->event.pressed) {
        layer_move(_LOWER);
      }
      return false;
      break;
    case RAISE_F:
      if (record->event.pressed) {
        layer_move(_RAISE);
      }
      return false;
      break;
  }
  // #ifdef CONSOLE_ENABLE
  //   print("KL:jjjj");
  // #endif 
    return true;
}

