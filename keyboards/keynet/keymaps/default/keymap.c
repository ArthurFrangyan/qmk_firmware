bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case ENT_SWP:
      if(record->event.pressed)
      {
        if (!record->tap.count) {
          swap_hands_on();
          return false;
        }
      }
      else
      {
        swap_hands_off();
      }
      return true;
    case QWERTY:
      if (record->event.pressed) {
        _PAD_index = _NUMPAD;
        set_single_persistent_default_layer(_QWERTY);
        layer_clear();
      }
      return false;
      break;
    case GEMINI:
      if (record->event.pressed) {
        // set_single_persistent_default_layer(_GEMINI);

        // layer_move(_GEMINI);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
        // layer_off(_SWP_HND);
      }
      return false;
      break;
    case SPC_LOW:
    case C_:
    case COMM_:
      if(record->event.pressed)
      {
        if (!record->tap.count) {
          layer_on(_LOWER);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
          return false;
        }
      }
      else
      {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return true;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {

        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case SPC_REZ:
    case BSPC_REZ:
    case X_:
    case DOT_:
      if(record->event.pressed)
      {
        if (!record->tap.count) {
          layer_on(_RAISE);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
          return false;
        }
      }
      else
      {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return true;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case MENU:
      if (record->event.pressed) {
        layer_on(_MENU);
      }
      else{
        layer_off(_MENU);
      }
      return false;
      break;
   case MENU_R:
      if (record->event.pressed) {
        layer_on(_MENU);
        menu_R = 1;
      }
      else{
        layer_off(_MENU);
        menu_R = 0;
      }
      return false;
      break;
    case GAME:
      if (record->event.pressed) {
        _PAD_index = _GAME;
        layer_move(_GAME);
      }
      return false;
      break;
    case GAME_TWO:
      if (record->event.pressed) {
        _PAD_index = _GAME_TWO;
        layer_move(_GAME_TWO);
      }
      return false;
      break;
    case GAME_N:
      if (record->event.pressed) {
        layer_on(_GAME_N);
      } else {
        layer_off(_GAME_N);
      }
      return false;
      break;
    case GAME_E:
      if (record->event.pressed) {
        if (IS_LAYER_ON(_GAME_E))
          layer_off(_GAME_E);
        else
          layer_on(_GAME_E);
      }
      return false;
      break;
    case NUMPAD:
      if (record->event.pressed) {
        layer_move(_NUMPAD);
      }
      return false;
      break;
    case BRUSH:
      if (record->event.pressed) {
        layer_move(_BRUSH);
      }
      return false;
      break;
    case LOWER_F:
      if (record->event.pressed) {
        layer_move(_LOWER);
      }
      return false;
      break;
    case RAISE_F:
      if (record->event.pressed) {
        layer_move(_RAISE);
      }
      return false;
      break;
    case PAD:
      if (record->event.pressed) {
        if (IS_LAYER_ON(_PAD_index))
          layer_off(_PAD_index);
        else
          layer_on(_PAD_index);
      }
      return false;
      break;
    /*case SFT_F:
      if (record->event.pressed) {
        register_code16(S(KC_F));
      }
      else {
        unregister_code16(S(KC_F));
      }
      return false;
      break;*/
  }
  // #ifdef CONSOLE_ENABLE
  //   print("KL:jjjj");
  // #endif 
    return true;
}

